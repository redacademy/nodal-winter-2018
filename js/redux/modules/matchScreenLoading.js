// Action types
const GET_BUTTON_LOADING = "GET_BUTTON_LOADING";
const GET_CONTENT_LOADING = "GET_CONTENT_LOADING";

// Action creators

export const getContentLoading = () => ({
  type: GET_CONTENT_LOADING
});

export const getButtonLoading = () => ({
  type: GET_BUTTON_LOADING
});

// Reducer
export default (
  state = { buttonLoading: true, contentLoading: true },
  action
) => {
  switch (action.type) {
    case GET_BUTTON_LOADING:
      return {
        ...state,
        buttonLoading: false
      };
    case GET_CONTENT_LOADING:
      return {
        ...state,
        contentLoading: false
      };
    default:
      return state;
  }
};
